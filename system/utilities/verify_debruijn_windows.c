bool verify_sequences_4color(void) {
    // Max windows = NUM_ROBOTS * NUM_LEDS
    uint32_t seen_count = 0;
    // We'll store codes in an array; each code fits in 4^6 = 4096 < 2^12, so uint16_t OK.
    // Worst-case storage = NUM_ROBOTS * NUM_LEDS entries; for 256*16=4096 entries -> ~8KB array of uint16_t+meta (still small)
    typedef struct { uint16_t code; uint16_t robot; uint16_t pos; } WEntry;
    static WEntry seen[NUM_ROBOTS * NUM_LEDS];

    for (uint16_t r = 0; r < NUM_ROBOTS; r++) {
        for (uint16_t i = 0; i < NUM_LEDS; i++) {
            uint16_t code = 0;
            for (uint16_t j = 0; j < WINDOW_SIZE; j++) {
                uint8_t v = debruijn_sequences[r][(i + j) % NUM_LEDS];
                code = (uint16_t)(code * 4 + (v & 0x3));
            }
            // linear search (simple). For faster verify, use hash table.
            for (uint32_t k = 0; k < seen_count; k++) {
                if (seen[k].code == code) {
                    printf("Collision: robot %u pos %u and robot %u pos %u (code=%u)\n",
                           r, i, seen[k].robot, seen[k].pos, code);
                    return false;
                }
            }
            seen[seen_count].code = code;
            seen[seen_count].robot = r;
            seen[seen_count].pos = i;
            seen_count++;
        }
    }
    printf("Verification passed: %u unique windows.\n", seen_count);
    return true;
}


int main(void) {
    if (verify_sequences_4color()) {
        printf("All sequences are valid and unique!\n");
    } else {
        printf("Error: duplicate window detected.\n");
    }
    return 0;
}

